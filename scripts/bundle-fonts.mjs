// Downloads the theme web fonts (latin + latin-ext woff2) from Google Fonts into
// src/assets/fonts/ and generates src/fonts.css, so the app renders them offline.
// Re-run with: node scripts/bundle-fonts.mjs
import { writeFileSync, mkdirSync } from "node:fs";

const UA =
  "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/122.0 Safari/537.36";
const CSS_URL =
  "https://fonts.googleapis.com/css2?family=VT323&family=Space+Grotesk:wght@400;500;600;700&family=Space+Mono:wght@400;700&family=JetBrains+Mono:wght@400;600&family=Antonio:wght@400;600;700&display=swap";
const KEEP = new Set(["latin", "latin-ext"]);
const OUT_DIR = new URL("../src/assets/fonts/", import.meta.url);
const FONTS_CSS = new URL("../src/fonts.css", import.meta.url);

const css = await (await fetch(CSS_URL, { headers: { "User-Agent": UA } })).text();

const blocks = [];
const re = /\/\*\s*([\w-]+)\s*\*\/\s*@font-face\s*{([^}]*)}/g;
let m;
while ((m = re.exec(css))) {
  const subset = m[1];
  const body = m[2];
  if (!KEEP.has(subset)) continue;
  const family = /font-family:\s*'([^']+)'/.exec(body)?.[1];
  const weight = /font-weight:\s*(\d+)/.exec(body)?.[1];
  const url = /url\(([^)]+)\)/.exec(body)?.[1];
  const range = /unicode-range:\s*([^;]+);/.exec(body)?.[1];
  if (family && weight && url) blocks.push({ subset, family, weight, url, range });
}

mkdirSync(OUT_DIR, { recursive: true });
let out = "/* Auto-generated by scripts/bundle-fonts.mjs — do not edit by hand. */\n\n";
for (const b of blocks) {
  const slug = b.family.toLowerCase().replace(/\s+/g, "-");
  const fname = `${slug}-${b.weight}-${b.subset}.woff2`;
  const buf = Buffer.from(
    await (await fetch(b.url, { headers: { "User-Agent": UA } })).arrayBuffer()
  );
  writeFileSync(new URL(fname, OUT_DIR), buf);
  out +=
    `@font-face {\n` +
    `  font-family: '${b.family}';\n` +
    `  font-style: normal;\n` +
    `  font-weight: ${b.weight};\n` +
    `  font-display: swap;\n` +
    `  src: url('./assets/fonts/${fname}') format('woff2');\n` +
    (b.range ? `  unicode-range: ${b.range};\n` : "") +
    `}\n`;
  console.log(`saved ${fname} (${buf.length} bytes)`);
}
writeFileSync(FONTS_CSS, out);
console.log(`\nwrote src/fonts.css (${blocks.length} font faces)`);
